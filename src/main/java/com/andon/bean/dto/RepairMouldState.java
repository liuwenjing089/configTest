package com.andon.bean.dto;

public class RepairMouldState {

	private int id;
	
	private String uuid;
	
	private int state;
	
	private int mouldTaskTimeCount;
	
	private int mouldTaskTimeResult;
	
	private int testMouldCount;
	
	private int testMouldToSucceed;
	
	private int cssNum;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getMouldTaskTimeCount() {
		return mouldTaskTimeCount;
	}

	public void setMouldTaskTimeCount(int mouldTaskTimeCount) {
		this.mouldTaskTimeCount = mouldTaskTimeCount;
	}

	public int getMouldTaskTimeResult() {
		return mouldTaskTimeResult;
	}

	public void setMouldTaskTimeResult(int mouldTaskTimeResult) {
		this.mouldTaskTimeResult = mouldTaskTimeResult;
	}

	public int getTestMouldCount() {
		return testMouldCount;
	}

	public void setTestMouldCount(int testMouldCount) {
		this.testMouldCount = testMouldCount;
	}

	public int getTestMouldToSucceed() {
		return testMouldToSucceed;
	}

	public void setTestMouldToSucceed(int testMouldToSucceed) {
		this.testMouldToSucceed = testMouldToSucceed;
	}

	public int getCssNum() {
		return cssNum;
	}

	public void setCssNum(int cssNum) {
		this.cssNum = cssNum;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	
	
}
